def generate_pptx(own_company: str, all_data: dict) -> bytes:
    """
    Build the full 11-slide PPTX and return raw bytes.
    all_data = { company_name: { "channel": {...}, "analytics": {...} }, ... }
    """
    prs = Presentation()
    prs.slide_width  = Inches(10)
    prs.slide_height = Inches(7.5)

    report_date = datetime.now().strftime("%d %B %Y")
    competitors = [c for c in all_data.keys() if c != own_company]

    slide_cover(prs, own_company, competitors, report_date)
    slide_executive_summary(prs, own_company, all_data)
    slide_channel_overview(prs, all_data)
    slide_subscribers_chart(prs, all_data)
    slide_top_videos(prs, all_data)
    slide_content_themes(prs, all_data)
    slide_posting_frequency(prs, all_data)
    slide_engagement(prs, all_data)
    slide_gap_analysis(prs, own_company, all_data)
    slide_recommendations(prs, own_company, all_data)
    slide_scoring(prs, all_data)
    slide_closing(prs, own_company, report_date)

    buf = io.BytesIO()
    prs.save(buf)
    return buf.getvalue()
