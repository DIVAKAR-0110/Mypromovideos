def slide_engagement(prs, all_data):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 0.9, C_CARD)
    _textbox(sl, "ENGAGEMENT ANALYSIS & SCORE", 0.4, 0.15, 9.2, 0.6,
             font_size=22, bold=True, color=C_PRIMARY)
    
    _textbox(sl, "Engagement Rate is the truest indicator of content resonance. High views with low engagement suggests clickbait or poor retention.", 0.4, 1.0, 9.2, 0.4, font_size=11, color=C_LIGHT)

    companies = list(all_data.keys())
    for i, company in enumerate(companies):
        a = all_data[company].get("analytics", {})
        col = i % 2
        row = i // 2
        lx = 0.4 + col * 4.7
        ty = 1.6 + row * 1.9
        _rect(sl, lx, ty, 4.4, 1.7, C_CARD)
        _textbox(sl, company, lx + 0.15, ty + 0.1, 4.1, 0.35,
                 font_size=14, bold=True, color=C_PRIMARY)
                 
        intel_score = a.get('intelligence_score', 0)
        _textbox(sl, f"Intel Score: {intel_score}/100", lx + 2.5, ty + 0.1, 1.8, 0.35,
                 font_size=12, bold=True, color=C_ACCENT, align=PP_ALIGN.RIGHT)
                 
        metrics = [
            ("Avg Views", _fmt(a.get("avg_views", 0))),
            ("Avg Likes", _fmt(a.get("avg_likes", 0))),
            ("Eng. Rate", f"{a.get('avg_engagement_rate', 0)}%"),
        ]
        mx = lx + 0.1
        for label, val in metrics:
            _textbox(sl, val, mx, ty + 0.6, 1.3, 0.4,
                     font_size=16, bold=True, color=C_PRIMARY, align=PP_ALIGN.CENTER)
            _textbox(sl, label, mx, ty + 1.0, 1.3, 0.3,
                     font_size=10, color=C_LIGHT, align=PP_ALIGN.CENTER)
            mx += 1.4
        if ty > 5.5:
            break

def slide_gap_analysis(prs, own, all_data):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 0.9, C_CARD)
    _textbox(sl, "STRATEGIC CONTENT GAPS", 0.4, 0.15, 9.2, 0.6,
             font_size=22, bold=True, color=C_PRIMARY)
    _textbox(sl, "Topics & formats competitors are actively covering that you are leaving on the table:",
             0.5, 1.0, 9.0, 0.4, font_size=12, color=C_LIGHT)

    all_themes: set = set()
    company_themes: dict = {}
    for company, data in all_data.items():
        themes = set(data.get("analytics", {}).get("content_themes", []))
        company_themes[company] = themes
        all_themes |= themes

    own_themes = company_themes.get(own, set())
    gaps = []
    for company, themes in company_themes.items():
        if company == own:
            continue
        missing = themes - own_themes
        if missing:
            gaps.append((company, list(missing)[:5]))

    top = 1.6
    if gaps:
        for comp, missing in gaps:
            _textbox(sl, f"► What {comp} covers, but {own} misses:",
                     0.5, top, 9.0, 0.32, font_size=13, bold=True, color=C_PRIMARY)
            top += 0.38
            _textbox(sl, "  " + "  |  ".join(missing),
                     0.6, top, 9.0, 0.28, font_size=11, color=C_ACCENT)
            top += 0.5
    else:
        _textbox(sl, "No significant content gaps found. You are covering the market well.",
                 0.5, top, 9.0, 0.4, font_size=12, color=C_PRIMARY)

