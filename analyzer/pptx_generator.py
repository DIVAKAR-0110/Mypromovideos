def slide_scoring(prs, all_data):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 0.9, C_CARD)
    _textbox(sl, "INTELLIGENCE RANKINGS & SCORECARD", 0.4, 0.15, 9.2, 0.6,
             font_size=22, bold=True, color=C_PRIMARY)
    _textbox(sl, "Intelligence Score factors in Audience Size, Engagement Quality, and Consistency.",
             0.4, 1.0, 9.2, 0.4, font_size=11, color=C_LIGHT)

    scores = sorted(
        [(c, d.get("analytics", {}).get("intelligence_score", 0)) for c, d in all_data.items()],
        key=lambda x: x[1], reverse=True
    )

    headers = ["Rank", "Company", "Subscribers", "Posts/Mo", "Eng.Rate", "Intel Score"]
    col_x = [0.3, 0.9, 3.2, 5.1, 6.5, 7.9]
    col_w = [0.6, 2.3, 1.8, 1.3, 1.3, 1.5]
    top = 1.6
    for i, h in enumerate(headers):
        _textbox(sl, h, col_x[i], top, col_w[i], 0.35,
                 font_size=12, bold=True, color=C_PRIMARY)
    _divider(sl, 2.0, C_PRIMARY)

    top = 2.2
    medal = ["🥇", "🥈", "🥉"]
    for rank, (company, sc) in enumerate(scores, 1):
        data = all_data[company]
        a = data.get("analytics", {})
        ch = data.get("channel", {})
        _rect(sl, 0.3, top, 9.4, 0.45,
              C_CARD if rank % 2 == 0 else C_BG)
        row = [
            medal[rank-1] if rank <= 3 else str(rank),
            company[:22],
            _fmt(ch.get("subscribers", 0)),
            str(a.get("posting_frequency_per_month", 0)),
            f"{a.get('avg_engagement_rate', 0)}%",
            str(sc),
        ]
        for i, val in enumerate(row):
            color = C_ACCENT if rank == 1 else C_PRIMARY
            bold = rank == 1
            _textbox(sl, val, col_x[i], top + 0.08, col_w[i], 0.3,
                     font_size=11, bold=bold, color=color)
        top += 0.5


