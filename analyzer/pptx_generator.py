"""
pptx_generator.py — Generates a professional 10-slide PowerPoint report.
"""

import io
from datetime import datetime
from pptx import Presentation
from pptx.util import Inches, Pt, Emu
from pptx.dml.color import RGBColor
from pptx.enum.text import PP_ALIGN
from pptx.chart.data import ChartData
from pptx.enum.chart import XL_CHART_TYPE


# ── Brand colors ──────────────────────────────────────────────────────────────
C_BG       = RGBColor(255, 249, 196)      # Light Yellow
C_CARD     = RGBColor(255, 255, 255)      # White
C_PRIMARY  = RGBColor(93, 64, 55)         # Brown
C_ACCENT   = RGBColor(255, 152, 0)        # Orange
C_WHITE    = RGBColor(255, 255, 255)
C_LIGHT    = RGBColor(121, 85, 72)        # Light Brown
C_GOLD     = RGBColor(255, 193, 7)
C_RED      = RGBColor(211, 47, 47)
C_TEXT     = RGBColor(93, 64, 55)         # Brown Text


def _rgb(color: RGBColor):
    return color


def _add_slide(prs, layout_idx=6):
    layout = prs.slide_layouts[layout_idx]
    return prs.slides.add_slide(layout)


def _fill_bg(slide, color: RGBColor):
    bg = slide.background
    fill = bg.fill
    fill.solid()
    fill.fore_color.rgb = color


def _textbox(slide, text, left, top, width, height,
             font_size=18, bold=False, color=None, align=PP_ALIGN.LEFT,
             font_name="Inter"):
    txb = slide.shapes.add_textbox(
        Inches(left), Inches(top), Inches(width), Inches(height))
    tf = txb.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.alignment = align
    run = p.add_run()
    run.text = text
    run.font.size = Pt(font_size)
    run.font.bold = bold
    run.font.name = font_name
    run.font.color.rgb = color or C_TEXT
    return txb


def _rect(slide, left, top, width, height, fill_color, alpha=None):
    shape = slide.shapes.add_shape(
        1,  # MSO_SHAPE_TYPE.RECTANGLE
        Inches(left), Inches(top), Inches(width), Inches(height))
    shape.fill.solid()
    shape.fill.fore_color.rgb = fill_color
    shape.line.fill.background()
    return shape


def _divider(slide, top, color=C_PRIMARY):
    _rect(slide, 0.5, top, 9.0, 0.04, color)


def _stat_box(slide, label, value, left, top, w=2.1, h=1.2):
    _rect(slide, left, top, w, h, C_CARD)
    _textbox(slide, value, left + 0.1, top + 0.1, w - 0.2, 0.55,
             font_size=20, bold=True, color=C_ACCENT, align=PP_ALIGN.CENTER)
    _textbox(slide, label, left + 0.1, top + 0.65, w - 0.2, 0.45,
             font_size=10, color=C_LIGHT, align=PP_ALIGN.CENTER)


def _fmt(n):
    if n >= 1_000_000:
        return f"{n/1_000_000:.1f}M"
    if n >= 1_000:
        return f"{n/1_000:.1f}K"
    return str(n)


