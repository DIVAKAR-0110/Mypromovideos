def slide_cover(prs, own, competitors, report_date):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 2.8, C_PRIMARY)
    _textbox(sl, "VIDEO COMPETITOR INTELLIGENCE", 0.4, 0.5, 9.2, 0.8,
             font_size=28, bold=True, color=C_WHITE, align=PP_ALIGN.CENTER)
    _textbox(sl, "EXECUTIVE ANALYSIS REPORT", 0.4, 1.2, 9.2, 0.6,
             font_size=20, color=C_GOLD, align=PP_ALIGN.CENTER)
    
    _textbox(sl, f"Your Company: {own}", 0.5, 3.3, 9, 0.5,
             font_size=16, bold=True, color=C_PRIMARY)
    comp_str = " | ".join(competitors)
    _textbox(sl, f"Competitors Analysed: {comp_str}", 0.5, 3.8, 9, 0.4,
             font_size=13, color=C_LIGHT)
             
    _divider(sl, 4.4, C_PRIMARY)
    
    _textbox(sl, f"Report Date: {report_date}    |    Generated by P2P Platform",
             0.5, 4.6, 9, 0.4, font_size=11, color=C_LIGHT)
    _textbox(sl, "CONFIDENTIAL / INTERNAL USE ONLY", 0.5, 5.2, 9, 0.35,
             font_size=10, bold=True, color=C_RED, align=PP_ALIGN.CENTER)


def slide_executive_summary(prs, own, all_data):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 0.9, C_PRIMARY)
    _textbox(sl, "EXECUTIVE SUMMARY", 0.4, 0.15, 9.2, 0.6,
             font_size=24, bold=True, color=C_WHITE)

    # Calculate some strategy stats
    leader = max(all_data.items(), key=lambda x: x[1].get("channel", {}).get("subscribers", 0), default=(own, {}))
    leader_name = leader[0]
    
    best_eng = max(all_data.items(), key=lambda x: x[1].get("analytics", {}).get("avg_engagement_rate", 0), default=(own, {}))
    best_eng_name = best_eng[0]

    summary_lines = [
        f"▶  Market Leader: {leader_name} currently commands the largest audience share.",
        f"▶  Engagement Winner: {best_eng_name} has the highest viewer interaction rate, showing stronger community loyalty.",
        f"▶  Strategic Focus for {own}: The data reveals significant gaps in publishing cadence and short-form video adaptation.",
        "▶  This report breaks down channel authority, content themes, and specific viewer engagement metrics.",
        "▶  Actionable marketing recommendations are detailed on slides 8-9 for immediate implementation.",
    ]
    top = 1.3
    for line in summary_lines:
        _textbox(sl, line, 0.5, top, 9.0, 0.42, font_size=13, color=C_PRIMARY)
        top += 0.55

    # Leader callout box
    subs = leader[1].get("channel", {}).get("subscribers", 0)
    _rect(sl, 0.5, top + 0.3, 9.0, 1.0, C_CARD)
    _textbox(sl, f"🏆  Dominant Channel: {leader_name}  |  {_fmt(subs)} Subscribers",
             0.7, top + 0.5, 8.6, 0.5, font_size=15, bold=True, color=C_PRIMARY)


def slide_channel_overview(prs, all_data):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 0.9, C_CARD)
    _textbox(sl, "CHANNEL OVERVIEW COMPARISON", 0.4, 0.15, 9.2, 0.6,
             font_size=22, bold=True, color=C_PRIMARY)
    
    _textbox(sl, "A high-level view of audience size and total video output. A high video count with low subscribers indicates poor content-market fit.", 0.4, 1.0, 9.2, 0.4, font_size=11, color=C_LIGHT)

    headers = ["Company", "Subscribers", "Total Videos", "Total Views", "Country"]
    col_w = [2.2, 1.7, 1.6, 1.9, 1.4]
    col_x = [0.3]
    for w in col_w[:-1]:
        col_x.append(col_x[-1] + w)

    top = 1.6
    for i, h in enumerate(headers):
        _textbox(sl, h, col_x[i], top, col_w[i], 0.35,
                 font_size=12, bold=True, color=C_PRIMARY)
    _divider(sl, 2.0, C_PRIMARY)

    top = 2.2
    for company, data in all_data.items():
        ch = data.get("channel", {})
        row = [
            company[:22],
            _fmt(ch.get("subscribers", 0)),
            _fmt(ch.get("total_videos", 0)),
            _fmt(ch.get("total_views", 0)),
            ch.get("country", "N/A"),
        ]
        bg = C_CARD if list(all_data.keys()).index(company) % 2 == 0 else C_BG
        _rect(sl, 0.3, top, 9.4, 0.45, bg)
        for i, val in enumerate(row):
            _textbox(sl, val, col_x[i], top + 0.08, col_w[i], 0.3,
                     font_size=11, color=C_PRIMARY)
        top += 0.5


def slide_subscribers_chart(prs, all_data):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 0.9, C_CARD)
    _textbox(sl, "SUBSCRIBER COUNT — BRAND REACH", 0.4, 0.15, 9.2, 0.6,
             font_size=22, bold=True, color=C_PRIMARY)

    cd = ChartData()
    cd.categories = list(all_data.keys())
    cd.add_series("Subscribers",
                  [d.get("channel", {}).get("subscribers", 0) for d in all_data.values()])

    chart = sl.shapes.add_chart(
        XL_CHART_TYPE.COLUMN_CLUSTERED,
        Inches(0.5), Inches(1.2), Inches(9.0), Inches(5.5), cd
    ).chart

    chart.has_legend = False
    chart.has_title = False
    
    # Color each individual bar point with a theme color
    if len(chart.series) > 0:
        series = chart.series[0]
        colors = [C_PRIMARY, C_ACCENT, C_GOLD, C_LIGHT, RGBColor(210, 180, 140)]
        for j, point in enumerate(series.points):
            fill = point.format.fill
            fill.solid()
            fill.fore_color.rgb = colors[j % len(colors)]


def slide_top_videos(prs, all_data):
    sl = _add_slide(prs)
    _fill_bg(sl, C_BG)
    _rect(sl, 0, 0, 10, 0.9, C_CARD)
    _textbox(sl, "TOP PERFORMING VIDEOS BY VIEWS", 0.4, 0.15, 9.2, 0.6,
             font_size=22, bold=True, color=C_PRIMARY)

    top = 1.3
    for company, data in all_data.items():
        analytics = data.get("analytics", {})
        top_vids = analytics.get("top_videos", [])[:2]
        _textbox(sl, f"► {company}", 0.4, top, 9.0, 0.3,
                 font_size=14, bold=True, color=C_PRIMARY)
        top += 0.38
        if not top_vids:
            _textbox(sl, "  No video data available.", 0.6, top, 8.8, 0.28,
                     font_size=11, color=C_LIGHT)
            top += 0.4
        else:
            for v in top_vids:
                title = v.get("title", "")[:60]
                views = _fmt(v.get("views", 0))
                likes = _fmt(v.get("likes", 0))
                txt = f"  📹 {title}  |  👁 {views} views  |  👍 {likes} likes"
                _textbox(sl, txt, 0.6, top, 9.0, 0.28, font_size=11, color=C_LIGHT)
                top += 0.35
        if top > 6.8:
            break


